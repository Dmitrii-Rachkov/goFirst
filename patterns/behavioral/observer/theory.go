/*
Наблюдатель
Также известен как: Издатель-Подписчик, Слушатель, Observer

 Суть паттерна

Наблюдатель — это поведенческий паттерн проектирования, который создаёт механизм подписки, позволяющий одним объектам
следить и реагировать на события, происходящие в других объектах.
*/

/*
Проблема

Представьте, что вы имеете два объекта: Покупатель и Магазин. В магазин вот-вот должны завезти новый товар,
который интересен покупателю.

Покупатель может каждый день ходить в магазин, чтобы проверить наличие товара. Но при этом он будет злиться,
без толку тратя своё драгоценное время.

С другой стороны, магазин может разослать спам каждому своему покупателю. Многих это расстроит, так как товар
специфический, и не всем он нужен.

Получается конфликт: либо покупатель тратит время на периодические проверки, либо магазин тратит ресурсы
на бесполезные оповещения.
*/

/*
Решение

Давайте называть Издателями те объекты, которые содержат важное или интересное для других состояние.
Остальные объекты, которые хотят отслеживать изменения этого состояния, назовём Подписчиками.

Паттерн Наблюдатель предлагает хранить внутри объекта издателя список ссылок на объекты подписчиков,
причём издатель не должен вести список подписки самостоятельно. Он предоставит методы, с помощью которых подписчики
могли бы добавлять или убирать себя из списка.

image_1.png

Теперь самое интересное. Когда в издателе будет происходить важное событие, он будет проходиться по списку подписчиков
и оповещать их об этом, вызывая определённый метод объектов-подписчиков.

Издателю безразлично, какой класс будет иметь тот или иной подписчик, так как все они должны следовать общему
интерфейсу и иметь единый метод оповещения.

image_2.png

Увидев, как складно всё работает, вы можете выделить общий интерфейс, описывающий методы подписки и отписки,
и для всех издателей. После этого подписчики смогут работать с разными типами издателей, а также получать оповещения
от них через один и тот же метод.
*/

/*
 Аналогия из жизни
После того как вы оформили подписку на газету или журнал, вам больше не нужно ездить в супермаркет и проверять,
не вышел ли очередной номер. Вместо этого издательство будет присылать новые номера по почте прямо к вам домой
сразу после их выхода.

Издательство ведёт список подписчиков и знает, кому какой журнал высылать. Вы можете в любой момент отказаться от
подписки, и журнал перестанет вам приходить.
*/

