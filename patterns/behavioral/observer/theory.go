/*
Отношения с другими паттернами

1. Цепочка обязанностей, Команда, Посредник и Наблюдатель показывают различные способы работы отправителей запросов
с их получателями:

- Цепочка обязанностей передаёт запрос последовательно через цепочку потенциальных получателей, ожидая, что какой-то
из них обработает запрос.
- Команда устанавливает косвенную одностороннюю связь от отправителей к получателям.
- Посредник убирает прямую связь между отправителями и получателями, заставляя их общаться опосредованно, через себя.
- Наблюдатель передаёт запрос одновременно всем заинтересованным получателям, но позволяет им динамически подписываться
или отписываться от таких оповещений.

2. Разница между Посредником и Наблюдателем не всегда очевидна. Чаще всего они выступают как конкуренты,
но иногда могут работать вместе.

- Цель Посредника — убрать обоюдные зависимости между компонентами системы. Вместо этого они становятся зависимыми
от самого посредника. С другой стороны, цель Наблюдателя — обеспечить динамическую одностороннюю связь, в которой одни
объекты косвенно зависят от других.

- Довольно популярна реализация Посредника при помощи Наблюдателя. При этом объект посредника будет выступать издателем,
а все остальные компоненты станут подписчиками и смогут динамически следить за событиями, происходящими в посреднике. В этом случае трудно понять, чем же отличаются оба паттерна.

- Но Посредник имеет и другие реализации, когда отдельные компоненты жёстко привязаны к объекту посредника. Такой код
вряд ли будет напоминать Наблюдателя, но всё же останется Посредником.

- Напротив, в случае реализации посредника с помощью Наблюдателя представим такую программу, в которой каждый компонент
системы становится издателем. Компоненты могут подписываться друг на друга, в то же время не привязываясь к конкретным классам. Программа будет состоять из целой сети Наблюдателей, не имея центрального объекта-Посредника.
*/
