/*
Применимость

1. Когда после изменения состояния одного объекта требуется что-то сделать в других, но вы не знаете наперёд, какие
именно объекты должны отреагировать.

- Описанная проблема может возникнуть при разработке библиотек пользовательского интерфейса, когда вам надо дать
возможность сторонним классам реагировать на клики по кнопкам.

Паттерн Наблюдатель позволяет любому объекту с интерфейсом подписчика зарегистрироваться на получение оповещений
о событиях, происходящих в объектах-издателях.

2. Когда одни объекты должны наблюдать за другими, но только в определённых случаях.

- Издатели ведут динамические списки. Все наблюдатели могут подписываться или отписываться от получения оповещений
прямо во время выполнения программы.
*/

/*
Шаги реализации

1. Разбейте вашу функциональность на две части: независимое ядро и опциональные зависимые части.
Независимое ядро станет издателем. Зависимые части станут подписчиками.

2. Создайте интерфейс подписчиков. Обычно в нём достаточно определить единственный метод оповещения.

3. Создайте интерфейс издателей и опишите в нём операции управления подпиской.
Помните, что издатель должен работать только с общим интерфейсом подписчиков.

3. Вам нужно решить, куда поместить код ведения подписки, ведь он обычно бывает одинаков для всех типов издателей.
Самый очевидный способ — вынести этот код в промежуточный абстрактный класс, от которого будут наследоваться все издатели.

4. Но если вы интегрируете паттерн в существующие классы, то создать новый базовый класс может быть затруднительно.
В этом случае вы можете поместить логику подписки во вспомогательный объект и делегировать ему работу из издателей.

5. Создайте классы конкретных издателей. Реализуйте их так, чтобы после каждого изменения состояния они отправляли
оповещения всем своим подписчикам.

6. Реализуйте метод оповещения в конкретных подписчиках. Не забудьте предусмотреть параметры, через которые издатель
мог бы отправлять какие-то данные, связанные с происшедшим событием.

Возможен и другой вариант, когда подписчик, получив оповещение, сам возьмёт из объекта издателя нужные данные.
Но в этом случае вы будете вынуждены привязать класс подписчика к конкретному классу издателя.

7. Клиент должен создавать необходимое количество объектов подписчиков и подписывать их у издателей.
*/

/*
Преимущества
- Издатели не зависят от конкретных классов подписчиков и наоборот.
- Вы можете подписывать и отписывать получателей на лету.
- Реализует принцип открытости/закрытости.
*/

